package Exercise03;



class Person {
    private String name;
    private int age;

    // Setters
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    // Getter
    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}

